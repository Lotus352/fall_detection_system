class History:
    def __init__(self, accelX, accelY, accelZ, gyroX, gyroY, gyroZ, timestamp, status, latitude, longitude):
        self.accelX = accelX
        self.accelY = accelY
        self.accelZ = accelZ
        self.gyroX = gyroX
        self.gyroY = gyroY
        self.gyroZ = gyroZ
        self.timestamp = timestamp
        self.status = status
        self.latitude = latitude
        self.longitude = longitude
